package com.mtss.bean;

public class EPC {
	int epcNo,activityNo,epcType,avail,inUse;
	String dateOfIssue;
	public int getEpcNo() {
		return epcNo;
	}
	public void setEpcNo(int epcNo) {
		this.epcNo = epcNo;
	}
	public int getActivityNo() {
		return activityNo;
	}
	public void setActivityNo(int activityNo) {
		this.activityNo = activityNo;
	}
	public int getEpcType() {
		return epcType;
	}
	public void setEpcType(int epcType) {
		this.epcType = epcType;
	}
	public int getAvail() {
		return avail;
	}
	public void setAvail(int avail) {
		this.avail = avail;
	}
	public int getInUse() {
		return inUse;
	}
	public void setInUse(int inUse) {
		this.inUse = inUse;
	}
	public String getDateOfIssue() {
		return dateOfIssue;
	}
	public void setDateOfIssue(String dateOfIssue) {
		this.dateOfIssue = dateOfIssue;
	}
	
	
}
